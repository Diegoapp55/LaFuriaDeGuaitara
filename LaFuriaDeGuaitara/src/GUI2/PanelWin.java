    @Override
    public void keyPressed(KeyEvent e) { 
        switch (e.getKeyCode()){
            case KeyEvent.VK_UP :
                if(l.getY() >= 48){
                    l.setLocation(l.getX(), l.getY() - velocidad);
                }else{
                    l.setLocation(l.getX(), l.getY() - 0);
                }
                break;
            
            case KeyEvent.VK_DOWN :
                if(l.getY() <= 480){
                    l.setLocation(l.getX(), l.getY() + velocidad);
                }else{
                    l.setLocation(l.getX(), l.getY() + 0);
                }
                break;
            
            case KeyEvent.VK_LEFT :
                if(l.getX() >= 169){
                    l.setLocation(l.getX() - velocidad, l.getY());
                }else{
                    l.setLocation(l.getX() - 0, l.getY());
                }
                break;
            
            case KeyEvent.VK_RIGHT :
                if(l.getX() <= 286){
                    l.setLocation(l.getX() + velocidad, l.getY());
                }else{
                    l.setLocation(l.getX() + 0, l.getY());
                }
                break;
                
            case KeyEvent.VK_SHIFT :
                velocidad = 10;
                break;

            case KeyEvent.VK_SPACE:
                if((l.getX() >= 252 && l.getX() <= 320) && (l.getY() >= 10 && l.getY() <= 76)){
                    fx.playItemEspecial();
                    int value = JOptionPane.showConfirmDialog(this, "Â¿Desea Pasar?" , 
                            "Punto de guardado", JOptionPane.YES_NO_OPTION);
                    if(value == JOptionPane.YES_OPTION){
                        Window.TEMA_CINE.stop();
                        Window.TEMA_JUEGO.stop();
                        Window.TEMA_JUEGO.playFinalGanar();
                        PanelCrearPartida.partida.setPanelActual(this);   
                        cargarGanar();
                    }else{
                        Window.TEMA_CINE.stop();
                        Window.TEMA_JUEGO.stop();
                        Window.TEMA_JUEGO.playFinalPerder();
                        cargarPerder();
                    }
                }
                //Item topaz
                if((l.getX() >= 149 && l.getX() <= 236) && (l.getY() >= 55 && l.getY() <= 105)){
                    esmeralda.setRecogido(true);
                    guardaPosX();
                    guardaPosY();
                    l.setLocation(guardaPosX(), guardaPosY());
                    esmeraldaObj.setVisible(false);
                    fx.playItem();
                    PanelCrearPartida.inventario.add(esmeralda);
                    l.setLocation(guardaPosX(), guardaPosY());
                }
                //Item esmeralda
                if((l.getX() >= 229 && l.getX() <= 316) && (l.getY() >= 470 && l.getY() <= 530)){
                    topaz.setRecogido(true);
                    guardaPosX();
                    guardaPosY();
                    l.setLocation(guardaPosX(), guardaPosY());
                    topazObj.setVisible(false);
                    fx.playItem();
                    PanelCrearPartida.inventario.add(topaz);
                    l.setLocation(guardaPosX(), guardaPosY());
                }
                break;
            
            
            case KeyEvent.VK_ESCAPE:
                PanelCrearPartida.partida.setPanelActual(this);
                guardaPosX();
                guardaPosY();
                fx.playPausa();
                cargarPausa();
                break;
            
            
            case KeyEvent.VK_I:
                PanelCrearPartida.partida.setPanelActual(this);
                guardaPosX();
                guardaPosY();
                fx.playPausa();
                cargarInventario();
                break;
            
        }
    }

